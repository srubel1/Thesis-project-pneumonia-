"""12. DELETION CURVE + AUC"""

def deletion_curve(model_fn, img, importance, steps=50):
    img_mod = img.copy()
    flat = importance.flatten()
    order = np.argsort(flat)[::-1]
    cls = np.argmax(model_fn(img[None])[0])
    scores = []
    H,W = img.shape[:2]

    for k in np.linspace(0, len(order), steps, dtype=int):
        if k > 0:
            coords = np.unravel_index(order[:k], (H,W))
            img_mod[coords] = 0
        scores.append(model_fn(img_mod[None])[0][cls])
    return scores

