def gradcam(model, img_rgb, conv_layer_name, preprocess_fn):
    img_prep = preprocess_fn(img_rgb.copy())
    img_prep = np.expand_dims(img_prep, 0)

    conv_layer = model.get_layer(conv_layer_name)

    grad_model = tf.keras.models.Model(
        [model.input],
        [conv_layer.output, model.output]
    )

    with tf.GradientTape() as tape:
        conv_out, preds = grad_model(img_prep)
        class_idx = tf.argmax(preds[0])
        loss = preds[:, class_idx]

    grads = tape.gradient(loss, conv_out)
    pooled = tf.reduce_mean(grads, axis=(0, 1, 2))
    conv_out = conv_out[0]

    heat = conv_out @ pooled[..., tf.newaxis]
    heat = np.squeeze(heat.numpy())
    heat = np.maximum(heat, 0)
    heat /= (heat.max() + 1e-8)

    heatmap = cv2.resize(heat, (img_rgb.shape[1], img_rgb.shape[0]))
    heatmap = cv2.applyColorMap(np.uint8(255 * heatmap), cv2.COLORMAP_JET)
    return heatmap

